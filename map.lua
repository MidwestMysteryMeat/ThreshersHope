--[[
    Map Module
    Defines the dungeon layout and provides tile access
    Supports multiple floors with stairs

    Tile types:
    0     = Empty (walkable)
    1-9   = Wall types (different textures)
    10    = Door (handled specially)
    11    = Stairs up
    12    = Stairs down
]]

local Map = {}

-- Reference to doors module for initialization
local Doors = require("doors")

function Map.create(themeName)
    local map = {
        floors = {},        -- Array of floor data
        currentFloor = 1,   -- Which floor player is on
        theme = themeName or "dungeon",
        spawnX = 2.5,
        spawnY = 2.5,
        spawnFloor = 1,
    }

    -- Generate map based on theme
    if themeName == "town" then
        map = Map.createTownMap()
    elseif themeName == "desert" then
        map = Map.createDesertMap()
    elseif themeName == "forest" then
        map = Map.createForestMap()
    elseif themeName == "void" then
        map = Map.createVoidMap()
    elseif themeName == "castle" then
        map = Map.createCastleMap()
    else
        map = Map.createDungeonMap()
    end

    -- Add methods
    setmetatable(map, {__index = Map})
    return map
end

-- Get current floor data
function Map:getData()
    return self.floors[self.currentFloor] or self.floors[1]
end

